public class basicUtil{
    public boolean isNull(Object obj){
        return obj == null;
    }
    
    public boolean notNull(Object obj){
        return obj != null;
    }
    
    public String blankIfNull(String str){
        return obj == null ? "" : str;
    }

    public String blankIfNull2(String str){
        return obj == null ? "" : str;
    }
    
    public String blankIfNull3(String str){
        return obj == null ? "" : str;
    }
}